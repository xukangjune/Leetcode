    def sampleStats(self, count):
        flag = True
        minNum = maxNum = mode = count[0]
        total = 0
        n = sum(count)
        k = 0
        j = n // 2
        first = second = 0
        modeNum = 0
        for i, num in enumerate(count):
            if num:
                if flag:
                    minNum = float(i)
                    flag = not flag
                maxNum = float(i)
                total += num * i
                if num > modeNum:
                    mode = float(i)
                    modeNum = num
                k += num
                if k >= j and not first:
                    first = i
                if k >= j+1 and not second:
                    second = i
        mean = total / n
        print(total)
        median = float(second) if n % 2 else (first+second) / 2
        print(second, first, j, n)
        return [minNum, maxNum, mean, median, mode]


